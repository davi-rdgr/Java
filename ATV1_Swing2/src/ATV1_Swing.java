    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (jTextField1.getText().isEmpty()) {
                throw new NumberFormatException("Caixa de texto sem valor!");
            }

            Double number = Double.parseDouble(jTextField1.getText());

            try {
                if (number > 500) {
                    String valorPercent = JOptionPane.showInputDialog(null, "Digite o desconto em porcentagem:", "Entrada de desconto", JOptionPane.QUESTION_MESSAGE);

                    if (valorPercent == null || valorPercent.isEmpty()) {
                        throw new NumberFormatException("Valor da porcentagem não inserido!");
                    }

                    Double novoValorPercent = Double.parseDouble(valorPercent);
                    Double porcentagem = number * (novoValorPercent / 100);
                    JOptionPane.showMessageDialog(null, "Valor com porcentagem de " + novoValorPercent + "% aplicada: " + (number - porcentagem));
                } else {
                    JOptionPane.showMessageDialog(null, "Sem opção de porcentagem para valores menores que $500.00");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Por favor, insira valores válidos: " + e.getMessage());
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Por favor, insira valores válidos: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

